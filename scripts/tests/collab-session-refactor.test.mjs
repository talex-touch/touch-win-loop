import assert from 'node:assert/strict'
import { readFile } from 'node:fs/promises'
import { resolve } from 'node:path'
import { it } from 'vitest'

const PROJECT_PAGE_FILE = resolve(process.cwd(), 'app/pages/team/[teamId]/project/[projectId].vue')
const USE_COLLAB_SESSION_FILE = resolve(process.cwd(), 'app/composables/useCollabSession.ts')
const WORKSPACE_MAIN_PANEL_FILE = resolve(process.cwd(), 'app/components/workspace/WorkspaceMainPanel.vue')
const WORKSPACE_RESOURCE_PREVIEW_TAB_FILE = resolve(process.cwd(), 'app/components/workspace/WorkspaceResourcePreviewTab.vue')
const RICH_TEXT_EDITOR_FILE = resolve(process.cwd(), 'app/components/editor/RichTextEditor.vue')
const RICH_TEXT_COMMANDS_FILE = resolve(process.cwd(), 'app/components/editor/rich-text-editor-commands.ts')

it('项目页使用 useCollabSession 统一协作状态机', async () => {
  const source = await readFile(PROJECT_PAGE_FILE, 'utf8')
  const collabSessionSource = await readFile(USE_COLLAB_SESSION_FILE, 'utf8')
  assert.match(source, /useCollabSession\(/, '项目页未接入 useCollabSession')
  assert.match(source, /collabSession\.handleRealtimeEnvelope\(message\)/, '项目页未复用协作实时消息处理')
  assert.match(source, /collabSession\.activateRoom\(\)/, '项目页未复用协作会话激活逻辑')
  assert.match(source, /const collabMarkdownDoc = collabSession\.markdownDoc/, '项目页未透传 markdown Y.Doc')
  assert.match(source, /const collabMarkdownAwareness = collabSession\.markdownAwareness/, '项目页未透传 markdown Awareness')
  assert.match(source, /const collabPreviewLoading = ref\(false\)/, '项目页缺少协作资料预览加载状态')
  assert.match(source, /if \(options\.openTab !== false\)\s+openPreviewSignal\.value \+= 1/, '协作资料仍未在加载前立即打开 tab')
  assert.match(source, /collabPreviewLoading\.value = true/, '协作资料打开时未进入加载态')
  assert.match(source, /:collab-markdown-doc="collabMarkdownDoc"/, '主面板未接入 markdown 文档对象')
  assert.match(source, /:collab-markdown-awareness="collabMarkdownAwareness"/, '主面板未接入 markdown Awareness')
  assert.match(source, /:collab-preview-loading="collabPreviewLoading"/, '主面板未接入协作预览加载状态')
  assert.match(source, /projectResourcePreviewRequestId = 0/, '项目页缺少资源预览请求序号守卫')
  assert.doesNotMatch(source, /update:collab-markdown-value/, '项目页仍保留旧的 Markdown 文本双向绑定')
  assert.doesNotMatch(source, /function startCollabSnapshotPollTimer/, '项目页仍保留旧的页面级轮询实现')

  assert.match(collabSessionSource, /const markdownDoc = computed\(\(\) => \{/, 'useCollabSession 未暴露 markdownDoc 计算属性')
  assert.match(collabSessionSource, /const markdownAwareness = computed\(\(\) => \{/, 'useCollabSession 未暴露 markdown Awareness 计算属性')
  assert.match(collabSessionSource, /ensureMarkdownCollabDocShape\(doc\)/, 'useCollabSession 未对 markdown 文档执行结构化迁移')
  assert.match(collabSessionSource, /syncMarkdownMirrorFromRichText\(doc\)/, 'useCollabSession 未维护 markdown 镜像同步')
})

it('markdown 协作文档已切换为公共富文本编辑器，旧双栏预览已移除', async () => {
  const panelSource = await readFile(WORKSPACE_MAIN_PANEL_FILE, 'utf8')
  const previewSource = await readFile(WORKSPACE_RESOURCE_PREVIEW_TAB_FILE, 'utf8')
  const editorSource = await readFile(RICH_TEXT_EDITOR_FILE, 'utf8')
  const commandsSource = await readFile(RICH_TEXT_COMMANDS_FILE, 'utf8')
  const markdownSection = previewSource.match(/<template v-if="props\.activePreviewMode === 'markdown'">([\s\S]*?)<\/template>/)?.[1] || ''

  assert.match(panelSource, /if \(activeResourceTab\.value\.previewMode === 'markdown'\)\s+return \['项目资料', title\]/, 'markdown breadcrumb 仍然挂在竞赛分析下')
  assert.match(previewSource, /<RichTextEditor[\s\S]*:doc="props\.collabMarkdownDoc"/, '资源预览页未接入公共富文本编辑器')
  assert.ok(markdownSection, '未找到 markdown 预览分支模板')
  assert.match(previewSource, /:awareness="props\.collabMarkdownAwareness"/, '资源预览页未向富文本编辑器传入 Awareness')
  assert.match(previewSource, /:current-user="props\.collabCurrentUser"/, '资源预览页未向富文本编辑器传入当前用户信息')
  assert.match(previewSource, /:show-toolbar="false"/, 'markdown 仍然显示顶部工具栏')
  assert.match(previewSource, /:enable-slash-menu="true"/, 'markdown 未启用 slash 菜单')
  assert.match(previewSource, /:image-upload-handler="props\.imageUploadHandler"/, 'markdown 未接入图片上传处理器')
  assert.match(previewSource, /:heading-levels="\[1, 2, 3, 4, 5, 6\]"/, 'markdown 未将标题能力扩展到 H1-H6')
  assert.match(previewSource, /@selection-change="(?:emit\('markdownSelectionChange', \$event\)|handleMarkdownSelectionChange)"/, '资源预览页未接收 markdown 本地选区事件')
  assert.match(previewSource, /@remote-presence-change="emit\('markdownRemotePresenceChange', \$event\)"/, '资源预览页未接收 markdown 远端选区事件')
  assert.doesNotMatch(markdownSection, /\{\{ activeResourceTab\.title \}\}/, 'markdown 顶部仍然显示文档标题')
  assert.doesNotMatch(markdownSection, /rev \{\{ Math\.max\(0, Number\(collabRevision \|\| 0\)\) \}\}/, 'markdown 顶部仍然显示修订版本')
  assert.doesNotMatch(markdownSection, /collabConnectionText/, 'markdown 分支仍然显示协作连接文案')
  assert.doesNotMatch(markdownSection, /CollabPresenceAvatarStack/, 'markdown 分支仍然保留第二条协作头')
  assert.doesNotMatch(previewSource, /Markdown 渲染预览/, '资源预览页仍保留旧的 Markdown 预览区')
  assert.doesNotMatch(previewSource, /workspace-markdown-preview__body/, '资源预览页仍保留旧的 Markdown 样式残留')
  assert.match(panelSource, /:collab-presence-users="showBreadcrumbPresence \? collabPresenceUsers : \[\]"/, '主面板未将 breadcrumb 协作头像栈透传给 chrome')
  assert.doesNotMatch(panelSource, /<CollabPresenceDock\s+:users="collabPresenceUsers"\s*\/>/, 'markdown 底部成员栏仍然存在')

  assert.match(editorSource, /createCollabMarkdownBaseExtensions\(\)/, '公共编辑器未复用共享 markdown schema 扩展')
  assert.match(editorSource, /Collaboration\.configure\(\{\s+document: doc,\s+field: 'prosemirror',/, '公共编辑器未接入 Yjs 协作片段')
  assert.match(editorSource, /yCursorPlugin\(awareness, \{/, '公共编辑器未接入 yCursorPlugin')
  assert.match(editorSource, /showToolbar\?: boolean/, '公共编辑器未提供工具栏显隐开关')
  assert.match(editorSource, /contentMaxWidth\?: number \| string/, '公共编辑器未提供正文最大宽度配置')
  assert.match(editorSource, /enableSlashMenu\?: boolean/, '公共编辑器未提供 slash 菜单开关')
  assert.match(editorSource, /imageUploadHandler\?: \(\(file: File\) => Promise<RichTextEditorImageUploadResult>\) \| null/, '公共编辑器未暴露图片上传处理器')
  assert.match(editorSource, /v-if="showToolbar"\s+class="rich-text-editor__toolbar"/, '公共编辑器未按显隐开关渲染工具栏')
  assert.match(editorSource, /--rich-text-editor-content-max-width/, '公共编辑器未通过变量限制正文最大宽度')
  assert.match(editorSource, /data-testid="rich-text-editor-selection-toolbar"/, '公共编辑器未渲染选区浮动工具栏')
  assert.match(editorSource, /const selectionToolbarInlineItems = computed\(\(\) => \{/, '公共编辑器未声明选区工具栏命令集')
  assert.match(editorSource, /function syncSelectionToolbar\(\): void \{/, '公共编辑器未同步选区工具栏位置')
  assert.match(editorSource, /material-symbols-outlined/, '公共编辑器未为选区工具栏接入 icon')
  assert.match(editorSource, /flex-wrap: nowrap;/, '公共编辑器的选区工具栏仍可换行')
  assert.match(editorSource, /preferredAbove >= 12[\s\S]*preferredBelow/, '公共编辑器未优先将选区工具栏放到选区外侧')
  assert.match(editorSource, /data-testid="rich-text-editor-inline-search"/, '公共编辑器未渲染文档内联搜索容器')
  assert.match(editorSource, /openInlineSearch\(\): boolean \{/, '公共编辑器未暴露统一文内搜索入口')
  assert.match(editorSource, /data-testid="rich-text-editor-heading-menu"/, '公共编辑器未渲染标题下拉菜单')
  assert.match(editorSource, /data-testid="rich-text-editor-slash-menu"/, '公共编辑器未渲染 slash 菜单浮层')
  assert.match(editorSource, /handlePaste: handleEditorPaste/, '公共编辑器未接入图片粘贴处理')
  assert.match(editorSource, /handleDrop: handleEditorDrop/, '公共编辑器未接入图片拖拽处理')
  assert.match(editorSource, /setCodeBlock\(\{ language: 'plaintext' \}\)/, '公共编辑器未为新建代码块设置默认语言')
  assert.match(editorSource, /insertUploadingImagePlaceholder\(/, '公共编辑器未插入图片上传占位节点')
  assert.match(editorSource, /scrollToOutlineHeading\(/, '公共编辑器未实现大纲跳转联动')
  assert.match(editorSource, /toggleHeadingMenu\(/, '公共编辑器未实现标题下拉交互')

  assert.match(commandsSource, /label: '正文'/, '统一命令注册表缺少正文命令')
  assert.match(commandsSource, /label: `H\$\{level\}`/, '统一命令注册表缺少标题命令')
  assert.match(commandsSource, /resolveHeadingIcon/, '统一命令注册表未为标题补齐 icon')
  assert.match(commandsSource, /label: '图片上传'/, '统一命令注册表缺少图片命令')
  assert.match(commandsSource, /action: 'image'/, '统一命令注册表未定义图片动作')
})

